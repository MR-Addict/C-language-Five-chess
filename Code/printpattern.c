{
	printf("************************\n");
	printf("*       *       *      *\n");
    printf("*   %c   *   %c   *   %c  *\n",a,b,c);
	printf("*       *       *      *\n");
    printf("************************\n");
	printf("*       *       *      *\n");
    printf("*   %c   *   %c   *   %c  *\n",d,e,f);
	printf("*       *       *      *\n");
	printf("************************\n");
    printf("*       *       *      *\n");
    printf("*   %c   *   %c   *   %c  *\n",g,h,i);
    printf("*       *       *      *\n");
	printf("************************\n");
}
